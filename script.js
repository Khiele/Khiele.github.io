

// Function to show img1 when buttonA is clicked
function showImg1() {
    let img1 = document.querySelector(".img1");
    let hide_img1 = document.querySelector(".hide_img1");

    img1.style.visibility = "visible";
    hide_img1.style.visibility = "visible";
}

// Function to hide img1 when buttonA is clicked
function hideImg1() {
    let img1 = document.querySelector(".img1");
    let hide_img1 = document.querySelector(".hide_img1");

    img1.style.visibility = "hidden";
    hide_img1.style.visibility = "hidden";
}

